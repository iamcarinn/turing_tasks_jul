console.log('Цикл while (...) { *…*}');

let i=0;
while (i<11) {
    console.log(i);
    i++;
}

///////////////////////////////////////////
console.log('Цикл do { *...* } while(...)');

let t=0;
do {
    console.log(t);
    t++;
} while (t<11);

///////////////////////////////////////////
console.log('Цикл for (...) { *...* }');

for (let x=0;x<11;x++) {
    console.log(x);
}

