/*Функция суммирования двух чисел*/
let sum=(a,b) => a+b;
alert('Сумма 1 и 2: '+ sum(1,2));

/*Функция разности двух чисел*/
let difference=(a,b) => a-b;
alert('Разница 5 и 3: '+ difference(5,3));

/*Функция умножения двух чисел*/
let multiplication=(a,b) => a*b;
alert('Умножение 4 на 3: '+ multiplication(4,3));

/*Функция деления двух чисел*/
let dividing=(a,b) => a/b;
alert('Деление 8 на 4: '+dividing(8,4));
