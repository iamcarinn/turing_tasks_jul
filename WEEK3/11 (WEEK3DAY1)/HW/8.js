/*Нестрогое сравнение переводит строковый тип в числовой*/
console.log(1=='1');
/*Строгое сравнение оставляет тип изначальным и при сравнении разных типов выдает false*/
console.log(1==='1');



/*Нестрогое сравнение переводит false (типа boolean) в числовой тип со значением 0*/
console.log(0==false);
/*Строгое сравнение оставляет тип изначальным и при сравнении разных типов выдает false*/
console.log(0===false);